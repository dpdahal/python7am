# what is variable?
# variable is a container for storing data values.
# variable is a name given to a memory location.

# rule for variable name
# 1. variable name must start with a letter or an underscore (_)
# 2. variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, _)
# 3. variable name are case-sensitive (name, Name, NAME are different variable)
# 4. variable name cannot start with a number.
# 5. variable name should not be a keyword.
# 6. variable name is meaningful.


